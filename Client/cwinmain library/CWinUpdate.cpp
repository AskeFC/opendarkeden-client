#include "CWinUpdate.h"


//-----------------------------------------------------------------------------
// Static members
//-----------------------------------------------------------------------------
DWORD	CWinUpdate::m_CurrentTime = timeGetTime();

//-----------------------------------------------------------------------------
//
// constructor / destructor
//
//-----------------------------------------------------------------------------
CWinUpdate::CWinUpdate()
{
	m_LastTime		= 0;
	m_UpdateDelay	= 0;
}

CWinUpdate::~CWinUpdate()
{
}

//-----------------------------------------------------------------------------
//
// member functions
//
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
// Update
//-----------------------------------------------------------------------------
void
CWinUpdate::Update()
{
	// 현재 시간
	SetCurrentTime();

	// 가장 최근에 Update()를 호출한 시간을 기억시켜 둔다.
	m_LastTime = m_CurrentTime;
}
